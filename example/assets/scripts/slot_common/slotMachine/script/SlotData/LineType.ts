/**
 * 线类型
 */
export enum LineType 
{
    /**
     * 连线
     */
    Line = 1,

    /**
     * 指定
     */
    Specified = 2,

    /**
     * 滚轴
     */
    Spin = 3,

    /**
     * 非首尾相连
     */
    NonHeadTail = 4,

    /**
     * 全局分散
     */
    GlobalScatter = 5,
}

